"""
There are times when you want to specify a type on to a variable. This can be achieved using casting.


"""

# Integers
print('\nInts')
x = int(4)
y = int(2.89)
z = int("40")

print(x)
print(y)
print(z)

# Floats
print('\nFloats')
x = float(4)
y = float(2.89)
z = float("40")
w = float("4.2")

print(x)
print(y)
print(z)
print(w)

# Strings
print('\nString')
x = str(4)
y = str(2.89)
z = str("40")
w = str("4.2")

print(x)
print(y)
print(z)
print(w)

"""
There are three numeric types in python
- int
- float
- complex
"""

x = 1  # int
y = 2.8  # float
z = 1j  # complex

# To view the type of any object in python, use the type() function
print(type(x))
print(type(x))
print(type(y))
print(type(z))

# int - Integer this can be positive or negative, without decimals of unlimited length#

x = 1
y = 324324354646345654623454352353252
z = -453452145543354654834593458349853485348534583458345348534853450998884847294954
print('\nInteger values:')
print(type(x))
print(type(y))
print(type(z))

# Float  or "floating point number" is a number of a positive or negative, containing one more decimals

x = 1.10
y = 1.0
z = -35.59
print('\nFloat values:')
print(type(x))
print(type(y))
print(type(z))

